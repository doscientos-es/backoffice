/**
 * Looks up province and city for a Spanish 5-digit ZIP.
 * Province is always returned when the prefix is valid.
 * City is returned when an exact match is found.
 */
export function lookupPostalCode(zip: string): { city: string | null; province: string | null } {
  const z = zip.trim().replace(/\s/g, "");
  if (!/^\d{5}$/.test(z)) return { city: null, province: null };
  const province = PROVINCE_BY_PREFIX[z.slice(0, 2)] ?? null;
  const city = CITY_BY_ZIP[z] ?? null;
  return { city, province };
}
